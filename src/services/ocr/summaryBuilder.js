/**
 * Generates summaries and simple explanations matching the classified document type.
 */
export function buildSummary(docType, fileName) {
  const summaries = {
    "Government Notice": `यह सरकारी नोटिस "${fileName}" के संदर्भ में जारी किया गया है। इसमें सार्वजनिक निर्देश या प्रशासनिक आदेश दिए गए हैं, जिसका समय पर अनुपालन आवश्यक है।`,
    "Electricity Bill": `यह बिजली का बिल है। कृपया कुल देय राशि और भुगतान की अंतिम तिथि का ध्यान रखें ताकि विलंब शुल्क से बचा जा सके।`,
    Insurance: `यह बीमा पॉलिसी दस्तावेज़ है। इसमें आपके कवरेज की नियम-शर्तें, प्रीमियम देय तिथि और दावा प्रक्रिया विवरण शामिल हैं।`,
    Loan: `यह ऋण समझौता पत्र (Loan Agreement) है। इसमें ब्याज दर, मासिक ईएमआई राशि और ऋण चुकाने की अवधि का उल्लेख है।`,
    Medical: `यह मेडिकल पर्ची या स्वास्थ्य रिपोर्ट है। इसमें दी गई दवाओं की खुराक या प्रयोगशाला परिणामों का विवरण शामिल है।`,
    "Aadhaar Card": `यह भारतीय विशिष्ट पहचान पत्र (Aadhaar Card) है। इसमें आपका नाम, जन्म तिथि, लिंग और अद्वितीय 12-अंकीय आधार संख्या अंकित है।`,
    "PAN Card": `यह स्थायी खाता संख्या (PAN Card) दस्तावेज़ है। यह वित्तीय लेनदेन और आयकर रिटर्न दाखिल करने के लिए अनिवार्य है।`,
    Employment: `यह रोजगार से संबंधित सैलरी स्लिप या जॉब ऑफर लेटर है। इसमें पदनाम, वेतन संरचना और भत्तों का विवरण है।`,
    Unknown: `यह एक सामान्य दस्तावेज़ है। हमने इसके मुख्य बिंदुओं को सारांशित किया है ताकि आप इसे आसानी से समझ सकें।`,
  };

  return {
    summary: summaries[docType] || summaries.Unknown,
    simpleExplanation:
      "यह दस्तावेज इंगित करता है कि नागरिक को सत्यापन या बिल के भुगतान के लिए समय सीमा से पहले आवश्यक कदम उठाने की जरूरत है।",
  };
}
